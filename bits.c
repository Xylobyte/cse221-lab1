/* 
 * allOddBits - return 1 if all odd-numbered bits in word set to 1
 *   Examples allOddBits(0xFFFFFFFD) = 0, allOddBits(0xAAAAAAAA) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allOddBits(int x) {
  /* Create template of ...10101010 across all 32 bits then AND the template 
    with our number to get like bits. Then comare that result to original template.
    A perfect match will satisfy the NOT XOR combo */
  int template = (170 << 8) | 170;
  template = (template << 16) | template;
  return !((x & template) ^ template);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  /* Shifting x n number of bytes over (8 * n bits) then ANDing it with 11111111 to 
    only highlight the bits that matter */
  int block = 255, byte;
  byte = (x >> (n << 3)) & block;
  return byte;
}
