/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
  /* By NOTing the word we can set all 0s to 1s. Then we can add 1 to it which should turn
    the 1s to 0s starting from the LSB until it hits a 0 which was originally a 1. This 
    should be the least bit. ANDing this result with the original word should give us a 
    1 at the least position */
  return (~x + 1) & x;
}
/* 
 * sign - return 1 if positive, 0 if zero, and -1 if negative
 *  Examples: sign(130) = 1
 *            sign(-23) = -1
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 10
 *  Rating: 2
 */
int sign(int x) {
  /* !!x gets a value of 1 if it's nonzero and sb is the sign of the word. Combining these
    gives -1 if negative, 1 if positive, and 0 if zero */
  int sb = x >> 31;
  return sb | !!x;
}
