/* 
 * thirdBits - return word with every third bit (starting from the LSB) set to 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 1
 */
int thirdBits(void) {
  /* create template of 1001 then overlay it across all 32 bits */
  int template = 9;
  template = (template << 6) | template;
  template = (template << 12) | template;
  template = (template << 9) | 73;
  return template;
}
/* 
 * bitNor - ~(x|y) using only ~ and & 
 *   Example: bitNor(0x6, 0x5) = 0xFFFFFFF8
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitNor(int x, int y) {
  /* Apply not then join them with an and to make sure both bits are 0 */
  return ~x & ~y;
}
